/// 中国 IPv4 地址范围匹配模块。
/// 基于 APNIC 委派数据，覆盖中国大陆主要 IP 段。
/// 使用预排序的 (start, end) u32 范围 + 二分查找。

use once_cell::sync::Lazy;

/// 将 IPv4 地址字符串转为 u32
const fn ip4(a: u8, b: u8, c: u8, d: u8) -> u32 {
    ((a as u32) << 24) | ((b as u32) << 16) | ((c as u32) << 8) | (d as u32)
}

/// 计算 CIDR 范围的起始和结束 u32
const fn cidr4(start: u32, prefix: u8) -> (u32, u32) {
    let mask = if prefix == 0 { 0 } else { !((1u32 << (32 - prefix)) - 1) };
    let net = start & mask;
    let end = net | !mask;
    (net, end)
}

/// 预计算的中国 IPv4 范围表，按 start 严格升序。
/// 每个元素为 (start_u32, end_u32)，end 为包含的最后一个地址。
static CHINA_IPV4_RANGES: Lazy<Vec<(u32, u32)>> = Lazy::new(|| {
    let raw = CHINA_IPV4_RAW;
    let mut v: Vec<(u32, u32)> = Vec::with_capacity(raw.len());
    for &(s, p) in raw {
        v.push(cidr4(s, p));
    }
    v.sort_by_key(|r| r.0);
    v
});

/// 判断给定的 IPv4 u32 是否在中国地址范围内
pub fn is_china_ipv4(ip: u32) -> bool {
    let ranges = &*CHINA_IPV4_RANGES;
    match ranges.binary_search_by_key(&ip, |r| r.0) {
        Ok(_) => true,
        Err(idx) => {
            if idx == 0 {
                false
            } else {
                let r = &ranges[idx - 1];
                ip >= r.0 && ip <= r.1
            }
        }
    }
}

